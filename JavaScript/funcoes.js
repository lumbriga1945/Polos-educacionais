function creatTable(territorio, posicao){
    $("#datagrid tr").remove();
    var table = document.getElementById('datagrid');
    for(var [key, value] of Object.entries(territorio[posicao])){
        var tr = document.createElement('tr');
        var td1 = document.createElement('td');
        var text1 = document.createTextNode(value);

        td1.appendChild(text1);
        tr.appendChild(td1);

        table.appendChild(tr);
    }
}

